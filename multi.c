#include <stdio.h>

int main() {
    int a, b, m;

    // Input first number
    printf("Enter the first no: ");
    scanf("%d", &a);

    // Input second number
    printf("Enter the second no: ");
    scanf("%d", &b);

    // Perform multiplication
    m = a * b;

    // Output the result
    printf("The product is %d\n", m);

    return 0;
}
